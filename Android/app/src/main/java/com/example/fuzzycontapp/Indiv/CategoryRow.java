package com.example.fuzzycontapp.Indiv;

public class CategoryRow {
    public String  name;
    public int img;

    public CategoryRow(String name, int img) {
        this.name = name;
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }
}
